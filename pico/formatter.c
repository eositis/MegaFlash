// Generate data for Block 0 - 21
//
// Input: blockNum    - Block Number (0-21)
//        blockBuffer - Buffer to store generated data (512 Bytes)
//        blockCount  - Block Count of the volume
//        volName     - Volume Name String
//        VolNameLen  - Volume Name Length
//
static void GenerateBlockData(uint32_t blockNum, uint8_t *blockBuffer,uint16_t blockCount,const char *volName,const uint32_t volNameLen) {
  assert(blockNum <= 21);
  assert(volNameLen<=15);
  
  const uint8_t *bootBlock = bootBlockPD203;
  
  //Block 0 - 1: Boot Loader
  if (blockNum == 0) {
    memcpy(blockBuffer, bootBlock, BLOCKSIZE);
    return;
  }

  if (blockNum == 1) {
    memcpy(blockBuffer, bootBlock+BLOCKSIZE, BLOCKSIZE);
    return;
  }

  //Block 2-5: Root Directory

  //Zero the buffer first
  if (blockNum >= 2 && blockNum <= 5) memset(blockBuffer, 0, BLOCKSIZE); 

  if (blockNum == 2) {
    memcpy(blockBuffer, block2, sizeof(block2));
    
    //Update Block Count
    blockBuffer[BLOCKCOUNTOFFSET]   = blockCount;
    blockBuffer[BLOCKCOUNTOFFSET+1] = blockCount>>8;
    
    //Update Volume Name
    if (volNameLen!=0) {
      strncpy(blockBuffer+VOLNAMEOFFSET, volName, 15);
      blockBuffer[VOLNAMELENOFFSET] = 0xf0 | (volNameLen&0x0f);
    }
    
    //Update Creation Date
    GetProdosTimestamp(blockBuffer+CREATETIONDATEOFFSET);
    
    return;
  }

  if (blockNum == 3) {
    memcpy(blockBuffer, block3, sizeof(block3));
    return;
  }

  if (blockNum == 4) {
    memcpy(blockBuffer, block4, sizeof(block4));
    return;
  }

  if (blockNum == 5) {
    memcpy(blockBuffer, block5, sizeof(block5));
    return;
  }

  //Block 6 - 21: Volume Bitmap
  if (blockNum >= 6 && blockNum <= 21) {
    BuildVolumeBitmap(blockNum - 6, blockBuffer, blockCount);
    return;
  }
}

