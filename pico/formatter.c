// Sanitize ProDOS Volume Name
// It makes sure the volume name is valid. i.e.
// 1) Maximum length = 15
// 2) Uppercase only
// 3) Only letters, digits and .(period)
// 4) Must begin with a letter.
//
// All invalid charactes are removed. If the result becomes
// an empty string, it sets the volume name to default.
//
// Input: volName - Volume Name string buffer
//
// Output: uint32_t - Length of the sanitized name
//
uint32_t SanitizeVolumeName(char* volName) {
  char c;
  
  //Remove all invalid characters
  uint j=0;
  for(uint i=0;i<FMT_VOLNAMEMAXLEN;++i) {
    c = volName[i];
    if (j==0 && !isalpha(c)) continue; //First char must be letter
    if (isalnum(c) || c=='.') {
      volName[j++] = toupper(c);
    }
    else if (c=='\0') break;
  }
  volName[j] ='\0';

  //If strlen == 0 or first char is not letter, set vol name to default
  if (strlen(volName)==0 || !isalpha(volName[0])) strcpy(volName,FMT_DEFAULTVOLNAME);
  
  return strlen(volName);
}

