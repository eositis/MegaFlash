// Build Volume Bitmap Blocks
//
// Input: bitmapBlockNum - Bitmap Block Number (0-N)
//        blockBuffer    - Buffer to store generated data (512 Bytes)
//        blockCount     - Block Count of the volume
//
static void BuildVolumeBitmap(uint32_t bitmapBlockNum, uint8_t* blockBuffer, uint16_t blockCount) {
  const uint32_t BITSPERBLOCK = BLOCKSIZE * 8;
  const uint8_t UNUSEDFILL = 0x00;  //If the block is not a bitmap block, fill it with 0x00

  const uint32_t quotient = blockCount / BITSPERBLOCK;  //Number of complete Bitmap Block
  const uint32_t remainder = blockCount % BITSPERBLOCK;
  
  if (bitmapBlockNum < quotient) {
    //Whole Block is used as Bitmap Block
    memset(blockBuffer, 0xFF, BLOCKSIZE);
  }
  else if (bitmapBlockNum == quotient) {
    if (remainder == 0) {
      //Not a bitmap block
      memset(blockBuffer, UNUSEDFILL, BLOCKSIZE);
    } else {
      //Only a portion of block is used as volume bitmap
      memset(blockBuffer, 0x00, BLOCKSIZE);
      const uint32_t q = remainder / 8;
      const uint32_t r = remainder % 8;
      memset(blockBuffer, 0xff, q);
      blockBuffer[q] = ~bitMask[r];
    }
  }
  else {// bitmapBlockNum > quotient
    //The block is not a bitmap block
    memset(blockBuffer, UNUSEDFILL, BLOCKSIZE);
  }


  if (bitmapBlockNum == 0) {
    //First Volume Bitmap Block
    //Mark the blocks used by Boot Loader, Volume Directory and Bitmaps as used
    uint32_t numBlockUsed = 6 + quotient + (remainder!=0?1:0);  //7-21
    uint32_t index = 0;
    while (1) {
      if (numBlockUsed >= 8) {
        blockBuffer[index++] = 0x00; 
        numBlockUsed -= 8;
      }
      else {
        blockBuffer[index] = bitMask[numBlockUsed];
        break;
      }
    }
  }
}

