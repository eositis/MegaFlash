// Format a unit
//
// Input: uintNum    - Unit Number
//        blockCount - Block Count of the unit
//        volName    - Volume Name
//        volNameLen - VOlume Name Length
//
// Output: success
//

bool FormatUnit(const uint32_t unitNum,const uint16_t blockCount,const char *volName,const uint32_t volNameLen) {
  uint8_t __attribute__((aligned(4))) blockBuffer[BLOCKSIZE];
  uint32_t blockAddress;
  bool success;
  uint error;
  
  if (blockCount<FMT_MINBLOCKCOUNT) return false; //Too small
  
  for(uint blockNum=0; blockNum<22; ++blockNum) {
    GenerateBlockData(blockNum, blockBuffer, blockCount, volName, volNameLen);
    error = WriteBlock(unitNum, blockNum, blockBuffer, NULL);
    if (error != MFERR_NONE) return false;
  }
  
  return true;
}



